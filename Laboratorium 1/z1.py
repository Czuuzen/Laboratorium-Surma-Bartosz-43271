liczba_1 = int(input("podaj 1 liczbę: "))
liczba_2 = int(input("podaj 2 liczbę: "))

print("Liczba pierwsza: " , liczba_1)
print("Liczba druga: " , liczba_2)

suma = liczba_1+liczba_2
iloraz = liczba_1/liczba_2
iloczyn = liczba_1*liczba_2
roznica = liczba_1-liczba_2

print("Suma wynosi: " , suma)
print("Iloraz wynosi: " , iloraz)
print("Iloczyn wynosi: " , iloczyn)
print("Różnica wynosi: " , roznica)